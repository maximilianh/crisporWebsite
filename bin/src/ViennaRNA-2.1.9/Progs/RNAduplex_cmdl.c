{
  return RNAduplex_cmdline_parser2 (argc, argv, args_info, 0, 1, 1);
}

int
RNAduplex_cmdline_parser_ext (int argc, char **argv, struct RNAduplex_args_info *args_info,
                   struct RNAduplex_cmdline_parser_params *params)
{
  int result;
  result = RNAduplex_cmdline_parser_internal (argc, argv, args_info, params, 0);

  if (result == EXIT_FAILURE)
    {
      RNAduplex_cmdline_parser_free (args_info);
      exit (EXIT_FAILURE);
    }
  
  return result;
}

int
RNAduplex_cmdline_parser2 (int argc, char **argv, struct RNAduplex_args_info *args_info, int override, int initialize, int check_required)
{
  int result;
  struct RNAduplex_cmdline_parser_params params;
  
  params.override = override;
  params.initialize = initialize;
  params.check_required = check_required;
  params.check_ambiguity = 0;
  params.print_errors = 1;

  result = RNAduplex_cmdline_parser_internal (argc, argv, args_info, &params, 0);

  if (result == EXIT_FAILURE)
    {
      RNAduplex_cmdline_parser_free (args_info);
      exit (EXIT_FAILURE);
    }
  
  return result;
}

